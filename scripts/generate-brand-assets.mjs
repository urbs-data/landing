// Generates every raster asset of the Urbs brand from the canonical SVGs in
// public/brand/:
//
//   node scripts/generate-brand-assets.mjs
//
//   - public/brand/wordmark-{light,dark}.png — hosted images referenced by the
//     email-signature HTML (email clients need a real URL).
//   - src/features/presentations/lib/brand-assets.ts — logo/wordmark as base64
//     data URIs baked into the .pptx generator (no runtime fs dependency).
//
// The brand SVGs already carry the wordmark as vector paths (no font needed),
// so we simply rasterize them with headless Chrome for the destinations that
// cannot consume SVG directly (email clients, .pptx).
import { execFileSync } from "node:child_process";
import { mkdtempSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";

const chromePath =
  "/Applications/Google Chrome.app/Contents/MacOS/Google Chrome";
const tempDir = mkdtempSync(join(tmpdir(), "urbs-brand-assets-"));
const brandDir = join(process.cwd(), "public", "brand");

function readSvg(file) {
  return readFileSync(join(brandDir, file), "utf8");
}

function svgDims(svg) {
  const w = Number(svg.match(/width="([\d.]+)"/)?.[1]);
  const h = Number(svg.match(/height="([\d.]+)"/)?.[1]);
  if (!w || !h) throw new Error("Could not read width/height from SVG");
  return { w, h };
}

// Rasterize an inline SVG at `scale`× its intrinsic size, on a transparent bg.
function shoot(name, pngPath, svg, scale) {
  const { w, h } = svgDims(svg);
  const htmlPath = join(tempDir, `${name}.html`);
  writeFileSync(
    htmlPath,
    `<!doctype html><html><head><meta charset="utf-8"><style>
    html,body{width:${w}px;height:${h}px;margin:0;overflow:hidden;background:transparent}
    svg{display:block;width:${w}px;height:${h}px}
  </style></head><body>${svg}</body></html>`,
  );
  execFileSync(
    chromePath,
    [
      "--headless",
      "--disable-gpu",
      "--hide-scrollbars",
      "--allow-file-access-from-files",
      "--default-background-color=00000000",
      `--force-device-scale-factor=${scale}`,
      `--window-size=${w},${h}`,
      `--screenshot=${pngPath}`,
      `file://${htmlPath}`,
    ],
    { stdio: "inherit" },
  );
  console.log(`✓ ${pngPath}`);
}

/* ---- Email signature wordmarks (served from public/brand/) ---- */

for (const mode of ["light", "dark"]) {
  shoot(
    `wordmark-${mode}`,
    join(brandDir, `wordmark-${mode}.png`),
    readSvg(`wordmark-${mode}.svg`),
    3,
  );
}

/* ---- Presentation brand module (base64, embedded in .pptx) ---- */

// Maps the .pptx asset keys to the canonical brand SVG that backs them. The new
// brand keeps the isotype purple on dark backgrounds, so "intensified" reuses
// the primary logo.
const presentationSources = {
  logoPrimary: "logo.svg",
  logoIntensified: "logo.svg",
  logoWhite: "logo-white.svg",
  wordmarkLight: "wordmark-light.svg",
  wordmarkDark: "wordmark-dark.svg",
  wordmarkWhite: "wordmark-white.svg",
};

const moduleBody = Object.entries(presentationSources)
  .map(([name, file]) => {
    const pngPath = join(tempDir, `${name}.png`);
    shoot(name, pngPath, readSvg(file), 8);
    const b64 = readFileSync(pngPath).toString("base64");
    return `  ${name}: "data:image/png;base64,${b64}",`;
  })
  .join("\n");

const modulePath = join(
  process.cwd(),
  "src",
  "features",
  "presentations",
  "lib",
  "brand-assets.ts",
);
writeFileSync(
  modulePath,
  `// AUTO-GENERATED by scripts/generate-brand-assets.mjs — do not edit by hand.
// Base64 data URIs of the Urbs logo / wordmark for embedding in .pptx files.
export const brandAssets = {
${moduleBody}
} as const;

export type BrandAsset = keyof typeof brandAssets;
`,
);
console.log(`✓ ${modulePath}`);

rmSync(tempDir, { recursive: true, force: true });
console.log("Done.");
